using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calling_Methods
{
    public class Math
    {
        public int Add(int data, int value)
        {
            return data + value;
        }

        public int Sub(int data, int value)
        {
            return data - value;
        }

        public int Div(int data, int value)
        {
            return data / value;
        }
    }
}
